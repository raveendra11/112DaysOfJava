package Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int number1 = 2;
        int number2 = 3;

        //addition
        System.out.println(number1 + number2);
        //subtraction
        System.out.println(number1 - number2);
        //multiplication
        System.out.println(number1 * number2);
        //division
        System.out.println(number2 / number1);
        //modulo
        System.out.println(number1 % number2);

    }
}
